from enum import Enum

SYSTEM_CAN_MESSAGE_ID_OFFSET = 4
SYSTEM_CAN_MESSAGE_PRIORITY_BIT = 0x400

class SystemCanDevice(int, Enum):
  SYSTEM_CAN_DEVICE_REAR_CONTROLLER = 0
  SYSTEM_CAN_DEVICE_IMU = 1
  SYSTEM_CAN_DEVICE_CAN_COMMUNICATION = 2
  SYSTEM_CAN_DEVICE_STEERING = 3
  SYSTEM_CAN_DEVICE_TELEMETRY = 4
  SYSTEM_CAN_DEVICE_FRONT_CONTROLLER = 5
  NUM_SYSTEM_CAN_DEVICES = 6
  
class SystemCanMessageId(int, Enum):
    SYSTEM_CAN_MESSAGE_REAR_CONTROLLER_TEST_DATA_ID = (20 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_REAR_CONTROLLER
    SYSTEM_CAN_MESSAGE_IMU_GYRO_DATA_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (51 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_IMU
    SYSTEM_CAN_MESSAGE_IMU_ACCEL_DATA_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (50 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_IMU
    SYSTEM_CAN_MESSAGE_CAN_COMMUNICATION_FAST_ONE_SHOT_MSG_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (55 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_CAN_COMMUNICATION
    SYSTEM_CAN_MESSAGE_CAN_COMMUNICATION_MEDIUM_ONE_SHOT_MSG_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (54 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_CAN_COMMUNICATION
    SYSTEM_CAN_MESSAGE_CAN_COMMUNICATION_SLOW_ONE_SHOT_MSG_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (53 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_CAN_COMMUNICATION
    SYSTEM_CAN_MESSAGE_STEERING_STEERING_STATE_ID = (5 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_STEERING
    SYSTEM_CAN_MESSAGE_TELEMETRY_TELEMETRY_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (52 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_TELEMETRY
    SYSTEM_CAN_MESSAGE_FRONT_CONTROLLER_FRONT_CONTROLLER_PEDAL_DATA_ID =  SYSTEM_CAN_MESSAGE_PRIORITY_BIT + (21 << SYSTEM_CAN_MESSAGE_ID_OFFSET) + SystemCanDevice.SYSTEM_CAN_DEVICE_FRONT_CONTROLLER